class Data_base():
    def __init__(self):
        self.data=np.array(pd.read_csv(r'data/element.csv'))[:,1:]

    def __getitem__(self, item):
        return self.data[item][:-1],self.classify_data(self.data[item][-1])

    def classify_data(self,data):
        if data >= 77:
            return 3
        elif 77>data > 40:
            return 2
        else:
            return 1
    def __len__(self):
        return len(self.data)


